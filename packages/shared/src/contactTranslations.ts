const CONTACT_BY_LANG: Record<AppLanguageCode, Record<string, string>> = {
  en: {},
  ar: CONTACT_AR,
  ur: CONTACT_UR,
  fa: CONTACT_FA,
};

/**
 * Merges default contact UI strings with language pack and optional tenant overrides.
 */
export function getContactUiStrings(
  language: string,
  overrides?: Record<string, string>
): Record<string, string> {
  const lang = normalizeAppLanguage(language);
  const localized = CONTACT_BY_LANG[lang] ?? {};
  return {
    ...DEFAULT_UI_STRINGS,
    ...localized,
    ...(overrides ?? {}),
  };
}
